#include <stddef.h>

void	*ft_memset(void *s, int c, size_t n)
{
	size_t		i;
	char		*s_c;

	i = 0;
	s_c = (char *)s;
	while (i < n)
	{
		s_c[i] = c;
		i++;
	}
	return (s);
}
